/**
 * add - Function checks for lowercase character.
 * @num1: Function takes a first parameter num1 whose data type is int
 * @num2: Second parameter, is also an integer
 * Description: Function adds two integers and returns the result.
 *
 * Return: An integer result from adding two number integers
 **/

int add(int num1, int num2)

{
	char n1 = num1;
	char n2 = num2;
	char result;
	result = n1 + n2;
	return (result);
}
